job('First-Maven_project-Via-DSL') {
   description("First Maven job generated by DSL on ${new Date()}")
    scm {
        github('akalam007/time-tracker', 'master')
    }
    triggers {
        scm('* * * * *')
    }
    steps {
        maven('clean package')
    }
    state('artifact') {
        archive 'target/*.war'
    }
   stage('deploy'){
   echo 'Deployement started'
      bat '''copy H:\\Jenkin\\jenkinworkspace\\workspace\\MavenProject\\web\\target\\*.war H:\\Jenkin\\apache-tomcat-8.0.30\\webapps\\'''
   }

}
